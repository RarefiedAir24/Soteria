import SwiftUI

extension CGFloat {
    // MARK: - Spacing Tokens
    /// 4pt - tight spacing
    static var spacingTight: CGFloat { 4 }
    
    /// 8pt - small gaps
    static var spacingSmall: CGFloat { 8 }
    
    /// 12pt - between inputs
    static var spacingInput: CGFloat { 12 }
    
    /// 16pt - standard spacing
    static var spacingStandard: CGFloat { 16 }
    
    /// 20pt - card/internal padding
    static var spacingCard: CGFloat { 20 }
    
    /// 24pt - between sections
    static var spacingSection: CGFloat { 24 }
    
    /// 32pt - screen-level spacing
    static var spacingScreen: CGFloat { 32 }
    
    /// 48pt - hero section padding
    static var spacingHero: CGFloat { 48 }
}

